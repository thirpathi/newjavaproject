package com.programs;

public class FiveTimesInFiveLines 
{
	
	public static void main(String[] args) 
	{
	 //display * symbols 5 times in 5 line
     for(int j=1;j<=5;j++)
     {
    	 for(int i=1;i<=5;i++)
    	 {
    		 System.out.print("*");
    	 }
		 System.out.println("");
     }
	}
}



